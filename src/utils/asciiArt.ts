export type ArtOptions = {
	uppercase?: boolean;
	spacing?: number;
	lineSpacing?: number;
	maxWidth?: number;
};

function splitText(text: string, maxWidth: number): string[] {
	const words = text.split(' ');
	const lines: string[] = [];
	let currentLine = '';

	for (const word of words) {
		if (currentLine.length + word.length + 1 <= maxWidth) {
			currentLine += (currentLine ? ' ' : '') + word;
		} else {
			if (currentLine) lines.push(currentLine);
			currentLine = word;
		}
	}

	if (currentLine) lines.push(currentLine);
	return lines;
}

function generateSingleLine(
	text: string,
	alphabet: Record<string, string[]>,
	lineCount: number,
	uppercase: boolean,
	spacing: number
): string {
	const normalizedText = uppercase ? text.toUpperCase() : text;
	const lines: string[] = Array(lineCount).fill('');
	const spacer = ' '.repeat(spacing);

	const fallback = alphabet[' '] ?? [];
	for (const char of normalizedText) {
		const asciiChar = alphabet[char] ?? fallback;
		for (let i = 0; i < lineCount; i++) {
			lines[i] += (asciiChar[i] ?? '') + spacer;
		}
	}

	return lines.join('\n');
}

export function generateAsciiArtText(text: string, options: ArtOptions = {}): string {
	const { uppercase = true, spacing = 0, lineSpacing = 1, maxWidth } = options;

	const lineCount = 6;

	if (maxWidth) {
		const lines = splitText(text, maxWidth);
		const blockSeparator = '\n'.repeat(1 + Math.max(1, lineSpacing));
		return lines
			.map((line) => generateSingleLine(line, ALPHABET, lineCount, uppercase, spacing))
			.join(blockSeparator);
	}

	return generateSingleLine(text, ALPHABET, lineCount, uppercase, spacing);
}
